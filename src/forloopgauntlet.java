
public class forloopgauntlet {
	public static void main(String[] args) {
		
		for(int i=0; i<=100; i++) {
			System.out.println(i);
			
		}
			
			
			
for(int m=100; m>=0; m--) {
	System.out.println(m);
}

for(int n=2; n<=100; n++) {
	if(n%2==0) {
		System.out.println(n);
	}
}
for(int b=1; b<=99; b++) {
	if(b%2==1) {
		System.out.println(b);
	}
}

	
	}

	
}
